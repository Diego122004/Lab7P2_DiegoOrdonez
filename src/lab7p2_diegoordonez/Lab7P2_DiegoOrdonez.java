    public void cargarArchivo() {
        Scanner sc = null;
        users = new ArrayList();
        if (archivo.exists()) {
            try {
                sc = new Scanner(archivo);
                String a = "";
                a += sc.next();
                a = a.replace("nombre:", "");
                a = a.replace("[", "");
                a = a.replace("]", "");
                a = a.replace(":", "");
                a = a.replace("usuario:", "");
                a = a.replace("contraseña:", "");
                String[] temp = a.split(",");
                while (sc.hasNext()) {
                    users.add(new Usuario(temp[0], temp[1], temp[2]));

                }

            } catch (Exception ex) {
            }
            sc.close();
        }//FIN IF
    }



